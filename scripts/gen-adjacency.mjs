// Generates src/data/adjacency.ts: country pairs sharing >= 2 border vertices.
import { readFileSync, writeFileSync } from 'node:fs';

const data = JSON.parse(readFileSync('public/data/world_1938.geojson', 'utf8'));

const pointOwners = new Map(); // "lon,lat" -> Set<name>
for (const f of data.features) {
  const name = f.properties.NAME;
  if (!name) continue;
  const polys = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates;
  for (const poly of polys) {
    for (const ring of poly) {
      for (const [lon, lat] of ring) {
        const key = lon.toFixed(4) + ',' + lat.toFixed(4);
        let set = pointOwners.get(key);
        if (!set) pointOwners.set(key, (set = new Set()));
        set.add(name);
      }
    }
  }
}

const pairCounts = new Map(); // "a||b" -> shared vertex count
for (const owners of pointOwners.values()) {
  if (owners.size < 2) continue;
  const names = [...owners].sort();
  for (let i = 0; i < names.length; i++) {
    for (let j = i + 1; j < names.length; j++) {
      const key = names[i] + '||' + names[j];
      pairCounts.set(key, (pairCounts.get(key) ?? 0) + 1);
    }
  }
}

const adj = {};
for (const [key, count] of pairCounts) {
  if (count < 2) continue; // require a real shared border, not a touch point
  const [a, b] = key.split('||');
  (adj[a] ??= []).push(b);
  (adj[b] ??= []).push(a);
}
for (const k of Object.keys(adj)) adj[k].sort();

const out =
  '// AUTO-GENERATED by scripts/gen-adjacency.mjs — do not edit by hand.\n' +
  '/** Land adjacency between basemap NAME ids (shared border vertices). */\n' +
  'export const ADJACENCY: Record<string, string[]> = ' +
  JSON.stringify(adj, null, 1) +
  ';\n';
writeFileSync('src/data/adjacency.ts', out);
console.log('countries with neighbors:', Object.keys(adj).length);
console.log('Germany:', adj['Germany']?.join(', '));
console.log('Turkey:', adj['Turkey']?.join(', '));
